class Loops {
    public static void main(String[] args)
    {
      //looping is started from here
     //for loop
      for(int i = 0 ; i < 5; i++)
      {
        System.out.println("*");
      }
      //while loop
      int i = 0;
      while(i < 5){
        System.out.println("hey");
        i++;
      }
      //do-while loop
      int j = 0;
      do{
         System.out.println("sunny");
         j++;
      }
      while(j<5);
     
    }
}
